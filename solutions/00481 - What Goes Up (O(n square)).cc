typedef long long ll;
typedef pair<int,int> pii;
typedef vector<int> vi;
typedef vector< pii > vii;
//ios::sync_with_stdio(0); cin.tie(0);

vi v;
vi LIS;

int sol() {
  int ibuf, len;
  len = LIS[0] = 1;

  for(int i = 1; i < SZ(LIS); i++) {
    ibuf = 0;
    for(int k = 0; k < i; k++) {
      if(v[k] < v[i]) {
        ibuf = max(ibuf, LIS[k]);
      }
    }

    LIS[i] = ibuf+1;
    len = max(len, LIS[i]);
  }

  return len;
}


int main(void)
{
  int buf, len;
  vi ans;

  while(cin>>buf)
    v.pb(buf);

  LIS.resize(v.size());

  len = sol();

  // print answer
  cout << len << endl << '-' << endl;

  for(int i = SZ(LIS)-1; len > 0 && i >= 0; i--) {
    if(LIS[i] == len) {
      ans.pb(v[i]);
      len--;
    }
  }

  for(auto it = ans.rbegin(); it != ans.rend(); it++) {
    cout << *it << endl;
  }
  
  return 0;
}
