typedef long long ll;
typedef pair<int,int> pii;
typedef vector<int> vi;
typedef vector< pii > vii;

//ios::sync_with_stdio(0); cin.tie(0);

string s;
string ss;

vector< vi > v(100, vi());

void sol() {
  int idx = 0;
  int lo = -1, hi;

  for(auto cur : ss) {
    int c = cur-'A';
    
    auto it = lower_bound(v[c].begin(), v[c].end(), idx);
    if(it == v[c].end()) {
      cout << "Not matched" << endl;
      return;
    }

    if(lo == -1) lo = *it;
    hi = idx = *it;
    idx++;
  }
  printf("Matched %d %d\n", lo,hi);
}

int main(void)
{
  getline(cin, s);
  int tc; cin >> tc;

  // cout << s << endl;;
  int idx = 0;
  for(auto c : s) {
    v[c-'A'].push_back(idx++);
  }

  while(tc--) {
    cin >> ss;
    
    // cout << ss << endl;;
    sol();
  }

  return 0;
}
